export type Language = 'en' | 'hi';
export type LocaleKey = keyof typeof locales.en;

export const useTranslation = (lang: Language) => {
  // Fallback to English if the provided language is not supported
  const safeLang = locales[lang] ? lang : 'en';
  return {
    t: (key: LocaleKey) => locales[safeLang][key] || key,
    lang: safeLang
  };
};
